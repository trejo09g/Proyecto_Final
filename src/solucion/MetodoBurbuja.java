    private void cmdOrdenarNumerosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdOrdenarNumerosActionPerformed
        // TODO add your handling code here:
        int[]Numeros=new int[10];
        for (int i=0;i<qq.getModel().getSize(); i++){
            String S=qq.getModel().getElementAt(i);
            Numeros[i]=Integer.parseInt(S);
        }
        for(int i=0; i< Numeros.length-1; i++){
            for(int j=0; j< Numeros.length-1; j++){
                if (Numeros[j]<Numeros[j+1]){
                    int tmp = Numeros[j+1];
                    Numeros[j+1]=Numeros[j];
                    Numeros[j]=tmp;
                }
            }
        }
        modelo1.removeAllElements();
        for(int i=0;i<qq.getModel().getSize(); i++){
            modelo1.addElement(Numeros[i]);
        }

    }//GEN-LAST:event_cmdOrdenarNumerosActionPerformed

